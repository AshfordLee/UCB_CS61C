# lui指令测试 - 检测U型立即数生成
# 测试不同的立即数值，验证是否正确左移12位

# 测试1: lui ra, 1 -> ra应该等于0x00001000 (4096)
lui ra, 1

# 测试2: lui sp, 4 -> sp应该等于0x00004000 (16384)  
lui sp, 4

# 测试3: lui gp, 0x123 -> gp应该等于0x00123000
lui gp, 0x123

# 测试4: lui tp, 0xFFFF -> tp应该等于0xFFFFF000
lui tp, 0xFFFF

# 测试5: lui t0, 0 -> t0应该等于0x00000000
lui t0, 0

# 测试6: lui t1, 0x80000 -> t1应该等于0x80000000 (最高位为1)
lui t1, 0x80000

