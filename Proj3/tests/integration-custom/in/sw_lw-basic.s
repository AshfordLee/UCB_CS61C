addi s0, x0, 200     # 基址
sw   x0, 80(s0)      # 先清零方便观察

# sb / lb
addi t0, x0, 102     # 0x66
sb   t0, 80(s0)
lb   a0, 80(s0)      # 预期 0x00000066

# sh / lh（半字对齐地址 82）
addi t1, x0, 1365    # 0x0555
sh   t1, 82(s0)
lh   a1, 82(s0)      # 预期 0x00000555

# 再做一个字节的符号扩展验证
addi t2, x0, -1      # 0xFF
sb   t2, 81(s0)
lb   a2, 81(s0)      # 预期 0xFFFFFFFF（-1）

# lw 验证组合结果（小端：80=66, 81=FF, 82=55, 83=05）
lw   a3, 80(s0)      # 预期 0x0555FF66